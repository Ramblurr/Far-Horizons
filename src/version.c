#include <string.h>
#include "engine.h"
#include "version.h"


int versionCommand(int argc, char *argv[]) {
    for (int i = 1; i < argc; i++) {
        char *opt = argv[i];
        char *val = NULL;
        for (val = opt; *val != 0; val++) {
            if (*val == '=') {
                *val = 0;
                val++;
                break;
            }
        }
        if (*val == 0) {
            val = NULL;
        }

        if (strcmp(opt, "--help") == 0 || strcmp(opt, "-h") == 0 || strcmp(opt, "-?") == 0) {
            fprintf(stderr, "usage: fh version\n");
            fprintf(stderr, "       display version of this program\n");
            return 2;
        } else {
            fprintf(stderr, "error: unknown option '%s%s%s'\n", opt, val ? "=" : "", val);
            return 2;
        }
    }

    printf("7.5.6\n");

    return 2;
}
